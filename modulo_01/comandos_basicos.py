# Imprime uma mensagem simples no console.
print("Olá, mundo!")

# Imprime o tipo de dado de uma string.
print(type("Olá, Python!"))

# Imprime o tipo de dado de um número inteiro.
print(type(100))

# Imprime o tipo de dado de um número decimal (float).
print(type(1.5))

# Imprime o tipo de dado de um valor booleano (Verdadeiro ou Falso).
print(type(True))