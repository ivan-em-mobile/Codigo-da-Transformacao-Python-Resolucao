"""
Operadores aritméticos: 
Crie programas que somam, subtraem, multiplicam, dividem 
e calculam o resto da divisão.
"""

# Definindo os dois números para as operações
numero1 = 15
numero2 = 4

# Soma
soma = numero1 + numero2
print(f"A soma de {numero1} e {numero2} é: {soma}")

# Subtração
subtracao = numero1 - numero2
print(f"A subtração de {numero1} e {numero2} é: {subtracao}")

# Multiplicação
multiplicacao = numero1 * numero2
print(f"A multiplicação de {numero1} e {numero2} é: {multiplicacao}")

# Divisão
divisao = numero1 / numero2
print(f"A divisão de {numero1} por {numero2} é: {divisao}")

# Módulo (resto da divisão)
resto = numero1 % numero2
print(f"O resto da divisão de {numero1} por {numero2} é: {resto}")