def somar(a, b):
    """
    Soma dois números.
    """
    return a + b

def subtrair(a, b):
    """
    Subtrai o segundo número do primeiro.
    """
    return a - b

def potencia(base, expoente):
    """
    Calcula a potência de um número.
    """
    return base ** expoente